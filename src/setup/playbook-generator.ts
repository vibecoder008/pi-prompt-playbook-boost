function formatGitAnalysis(g: GitAnalysis): string {
  const parts: string[] = ["### Git History Analysis\n"];

  parts.push(`- Total commits scanned: ${g.totalCommits}`);
  parts.push(`- Authors: ${g.authors.join(", ") || "none"}`);
  if (g.dateRange) {
    parts.push(
      `- Date range: ${g.dateRange.earliest} to ${g.dateRange.latest}`
    );
  }
  parts.push(
    `- Clean commit rate: ${(g.cleanCommitRate * 100).toFixed(1)}%`
  );

  // Fix chains
  if (g.fixChains.length > 0) {
    parts.push(`\n**Fix-After-Feature Chains (${g.fixChains.length}):**`);
    const shown = g.fixChains.slice(0, 30);
    for (const fc of shown) {
      const files = fc.sharedFiles.slice(0, 5).join(", ");
      const extra = fc.sharedFiles.length > 5 ? ` +${fc.sharedFiles.length - 5} more` : "";
      parts.push(
        `- "${fc.featureCommit.message}" → fix: "${fc.fixCommit.message}" (${fc.hoursBetween.toFixed(1)}h) | files: ${files}${extra}`
      );
    }
    if (g.fixChains.length > 30) {
      parts.push(`- ... and ${g.fixChains.length - 30} more`);
    }
  } else {
    parts.push("\nNo fix-after-feature chains detected.");
  }

  // Coupling rules
  if (g.couplingRules.length > 0) {
    parts.push(
      `\n**Co-Change Coupling Rules (${g.couplingRules.length}):**`
    );
    const shown = g.couplingRules.slice(0, 20);
    for (const cr of shown) {
      parts.push(
        `- ${cr.fileA} ↔ ${cr.fileB} | ` +
          `${cr.coOccurrences} co-occurrences, ` +
          `${(cr.couplingRate * 100).toFixed(0)}% coupling`
      );
    }
    if (g.couplingRules.length > 20) {
      parts.push(`- ... and ${g.couplingRules.length - 20} more`);
    }
  }

  // Hotspots
  if (g.hotspots.length > 0) {
    parts.push("\n**Hotspot Files (top 15):**");
    const shown = g.hotspots.slice(0, 15);
    for (const h of shown) {
      parts.push(
        `- ${h.path} | ${h.commitCount} commits, ${h.fixCount} fixes`
      );
    }
  }

  return parts.join("\n");
}

function formatSessionAnalysis(s: SessionAnalysis): string {
  const parts: string[] = ["### Pi Session Analysis\n"];

  if (s.totalSessions === 0) {
    parts.push("No pi session history available.");
    return parts.join("\n");
  }

  parts.push(`- Sessions analyzed: ${s.totalSessions}`);
  parts.push(`- Total messages: ${s.totalMessages}`);
  parts.push(`- User messages: ${s.totalUserMessages}`);
  parts.push(`- Avg turns per session: ${s.avgTurnsPerSession.toFixed(1)}`);
  parts.push(`- Retries detected: ${s.retryCount}`);
  parts.push(`- Tool errors: ${s.toolErrorCount}`);

  if (s.promptPatterns.length > 0) {
    parts.push("\n**Common Prompt Patterns:**");
    const shown = s.promptPatterns.slice(0, 15);
    for (const p of shown) {
      parts.push(
        `- "${p.pattern}" — ${p.count} times, avg ${p.avgTurns} turns`
      );
    }
  }

  return parts.join("\n");
}

function formatCodebaseAnalysis(c: CodebaseAnalysis): string {
  const parts: string[] = ["### Codebase Analysis\n"];

  // Stack
  parts.push("**Tech Stack:**");
  parts.push(`- Language: ${c.stack.language}`);
  if (c.stack.framework) parts.push(`- Framework: ${c.stack.framework}`);
  if (c.stack.orm) parts.push(`- ORM: ${c.stack.orm}`);
  if (c.stack.styling) parts.push(`- Styling: ${c.stack.styling}`);
  if (c.stack.stateManagement)
    parts.push(`- State management: ${c.stack.stateManagement}`);
  if (c.stack.packageManager)
    parts.push(`- Package manager: ${c.stack.packageManager}`);
  parts.push(`- Monorepo: ${c.stack.monorepo ? "yes" : "no"}`);

  if (c.testFramework) parts.push(`- Test framework: ${c.testFramework}`);

  // Build / lint commands
  if (c.buildCommands.length > 0) {
    parts.push("\n**Build Commands:**");
    for (const cmd of c.buildCommands) parts.push(`- ${cmd}`);
  }
  if (c.lintCommands.length > 0) {
    parts.push("\n**Lint Commands:**");
    for (const cmd of c.lintCommands) parts.push(`- ${cmd}`);
  }

  // Key directories
  if (c.keyDirectories.length > 0) {
    parts.push("\n**Key Directories:**");
    const shown = c.keyDirectories.slice(0, 20);
    parts.push(shown.join(", "));
  }

  // File patterns
  if (c.filePatterns.length > 0) {
    parts.push("\n**File Extension Distribution:**");
    const shown = c.filePatterns.slice(0, 10);
    for (const fp of shown) {
      parts.push(`- ${fp.extension}: ${fp.count} files`);
    }
  }

  // Existing rules
  if (c.existingRules.length > 0) {
    parts.push("\n**Existing AI Rules:**");
    for (const rule of c.existingRules) {
      // Include up to 2000 chars of each rule file.
      const preview =
        rule.content.length > 2000
          ? rule.content.slice(0, 2000) + "\n[...truncated]"
          : rule.content;
      parts.push(`\nSource: ${rule.source} (${rule.path})`);
      parts.push("```");
      parts.push(preview);
      parts.push("```");
    }
  }

  // Linter / formatter config
  if (c.linterConfig) {
    parts.push(`\n**Linter/Formatter Configuration:**`);
    parts.push(`- Tool: ${c.linterConfig.tool}`);
    parts.push(`- Config: ${c.linterConfig.configPath}`);
    parts.push("```");
    parts.push(c.linterConfig.content);
    parts.push("```");
  }

  // Import aliases
  const aliasEntries = Object.entries(c.importAliases);
  if (aliasEntries.length > 0) {
    parts.push("\n**Import Aliases (tsconfig paths):**");
    for (const [alias, target] of aliasEntries) {
      parts.push(`- ${alias} → ${target}`);
    }
  }

  // CI/CD workflows
  if (c.ciWorkflows.length > 0) {
    parts.push(`\n**CI/CD Workflows:**`);
    for (const wf of c.ciWorkflows) {
      parts.push(`\nWorkflow: ${wf.name} (${wf.path})`);
      parts.push("```yaml");
      parts.push(wf.content);
      parts.push("```");
    }
  }

  // Environment variables
  if (c.envVars.length > 0) {
    parts.push("\n**Required Environment Variables (.env.example):**");
    for (const v of c.envVars) {
      parts.push(`- ${v}`);
    }
  }

  // Project documentation
  if (c.projectDocs.length > 0) {
    parts.push("\n**Project Documentation:**");
    for (const doc of c.projectDocs) {
      const preview =
        doc.content.length > 3000
          ? doc.content.slice(0, 3000) + "\n[...truncated]"
          : doc.content;
      parts.push(`\nSource: ${doc.source} (${doc.path})`);
      parts.push("```markdown");
      parts.push(preview);
      parts.push("```");
    }
  }

  // Dependencies (top 30)
  if (c.stack.dependencies.length > 0) {
    parts.push("\n**Dependencies (top 30):**");
    parts.push(c.stack.dependencies.slice(0, 30).join(", "));
    if (c.stack.dependencies.length > 30) {
      parts.push(`... and ${c.stack.dependencies.length - 30} more`);
    }
  }

  return parts.join("\n");
}

