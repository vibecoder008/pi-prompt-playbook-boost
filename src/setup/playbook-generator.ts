/**
 * Build the LLM prompt that asks the model to generate a project-specific
 * playbook from the raw analysis data.
 *
 * The returned string is intended to be sent to the current model via
 * `pi.sendUserMessage()`.
 */
export function generatePlaybookPrompt(
  gitAnalysis: GitAnalysis | null,
  sessionAnalysis: SessionAnalysis | null,
  codebaseAnalysis: CodebaseAnalysis
): string {
  const sections: string[] = [];

  // -- Instruction header ---------------------------------------------------
  sections.push(trimBlock(`
    Generate a project-specific prompt playbook in Markdown format.
    Use the analysis data below to fill every section with REAL, SPECIFIC
    information from this project.  Use actual file paths, actual command
    names, actual framework conventions.  Do NOT be generic.

    Output ONLY the Markdown playbook — no preamble, no explanation.
  `));

  // -- Playbook template structure ------------------------------------------
  sections.push(trimBlock(`
    ## Playbook Template

    Follow this exact structure:

    \`\`\`markdown
    # Project Playbook
    > Auto-generated by pi-prompt-playbook-boost. Last updated: {{DATE}}
    > Based on: {{COMMIT_COUNT}} commits, {{SESSION_COUNT}} sessions, {{FILE_COUNT}} source files

    ## Project Identity
    - **Stack**: {{STACK}}
    - **Test runner**: {{TEST_RUNNER}}
    - **Package manager**: {{PKG_MANAGER}}
    - **Monorepo**: {{MONOREPO}}
    - **Key directories**: {{KEY_DIRS}}

    ## Prompt Structure

    ### 1. WHAT — Clear Deliverable
    State exactly what to build. Be specific about the end result.

    ### 2. WHERE — File Locations
    Name the files to create or modify.

    ### 3. CONNECTS — Integration Points
    List every system this feature touches.
    [Project-specific examples from the stack.]

    ### 4. GUARDS — Constraints & Edge Cases
    State what must NOT happen, validation rules, error cases.

    ### 5. VERIFY — Definition of Done
    How to confirm the work is complete.
    [Project-specific verification commands.]

    ## Mandatory Checklist
    [Auto-generated from failure analysis — items that when omitted cause fixes]
    - [ ] Item 1
    - [ ] Item 2

    ## Conventions
    [Auto-extracted from codebase and existing rules]
    - Convention 1
    - Convention 2

    ## Co-Change Rules
    [From git coupling analysis]
    - When modifying X → also update Y
    - ...

    ## Known Failure Patterns
    [From fix-after-feature chain analysis]
    - Pattern → caused N fix commits

    ## AI-Specific Anti-Patterns
    [Things the AI specifically gets wrong in this project]
    - Anti-pattern 1

    ## Success Patterns
    [Prompt structures that historically led to clean first-attempt code]
    - Pattern → success rate

    ## Stats
    - Total boosted prompts: 0
    - First-attempt success rate: N/A
    - Most effective template: N/A
    - Most problematic area: N/A
    - Last updated: {{DATE}}
    \`\`\`
  `));

  // -- Raw analysis data ----------------------------------------------------
  sections.push("## Analysis Data\n");
  sections.push(gitAnalysis ? formatGitAnalysis(gitAnalysis) : "### Git History\nNo git history available (new project).\n");
  sections.push(sessionAnalysis ? formatSessionAnalysis(sessionAnalysis) : "### Pi Sessions\nNo session history available.\n");
  sections.push(formatCodebaseAnalysis(codebaseAnalysis));

  // -- Final instructions ---------------------------------------------------
  sections.push(trimBlock(`
    ## Instructions

    1. Replace every {{PLACEHOLDER}} with real data from the analysis above.
    2. For "Mandatory Checklist": derive items from the fix chains — each
       recurring fix pattern becomes a checklist item.
    3. For "Conventions": merge data from the tech stack, existing rules, and
       file patterns.  Be specific (e.g. "Use \`@/\` import alias" not
       "Use consistent imports").
    4. For "Co-Change Rules": translate coupling rules into actionable
       sentences (e.g. "When modifying prisma/schema.prisma → run prisma
       generate").
    5. For "Known Failure Patterns": convert fix chains into warnings with
       real file paths and counts.
    6. For "Success Patterns": use clean commit rate and session data to
       identify what works.
    7. For "AI-Specific Anti-Patterns": infer from session retries and tool
       errors what the AI commonly gets wrong.
    8. If a section has no data (e.g. no sessions yet), write:
       "No data yet — this section populates as you work."
    9. Keep the playbook under 10 KB.  Be dense, not verbose.
  `));

  return sections.join("\n\n");
}

/**
 * Build a concise, human-readable summary of the analysis results.
 * Shown to the user as a notification after setup completes.
 */
export function buildAnalysisSummary(
  gitAnalysis: GitAnalysis | null,
  sessionAnalysis: SessionAnalysis | null,
  codebaseAnalysis: CodebaseAnalysis
): string {
  const lines: string[] = [];

  // Stack
  const stackParts: string[] = [];
  if (codebaseAnalysis.stack.language !== "unknown")
    stackParts.push(codebaseAnalysis.stack.language);
  if (codebaseAnalysis.stack.framework)
    stackParts.push(codebaseAnalysis.stack.framework);
  if (codebaseAnalysis.stack.orm) stackParts.push(codebaseAnalysis.stack.orm);
  if (codebaseAnalysis.stack.styling)
    stackParts.push(codebaseAnalysis.stack.styling);

  if (stackParts.length > 0) {
    lines.push(`Tech stack: ${stackParts.join(", ")}`);
  }

  if (codebaseAnalysis.testFramework) {
    lines.push(`Test framework: ${codebaseAnalysis.testFramework}`);
  }

  // Git
  if (gitAnalysis && gitAnalysis.totalCommits > 0) {
    lines.push(
      `${gitAnalysis.totalCommits} commits analyzed` +
        ` → ${gitAnalysis.fixChains.length} fix-after-feature chains found`
    );
  } else {
    lines.push("No git history (new project)");
  }

  // Sessions
  if (sessionAnalysis && sessionAnalysis.totalSessions > 0) {
    lines.push(
      `${sessionAnalysis.totalSessions} pi sessions` +
        ` → ${sessionAnalysis.totalUserMessages} prompts analyzed`
    );
  } else {
    lines.push("No pi session history found");
  }

  // Coupling rules
  if (gitAnalysis && gitAnalysis.couplingRules.length > 0) {
    lines.push(
      `${gitAnalysis.couplingRules.length} co-change coupling rules detected`
    );
  }

  // Existing rules
  if (codebaseAnalysis.existingRules.length > 0) {
    const sources = codebaseAnalysis.existingRules.map(
      (r: { source: string }) => r.source
    );
    lines.push(`Existing rules imported from: ${sources.join(", ")}`);
  }

  // Linter config
  if (codebaseAnalysis.linterConfig) {
    lines.push(`Linter: ${codebaseAnalysis.linterConfig.tool}`);
  }

  // CI workflows
  if (codebaseAnalysis.ciWorkflows.length > 0) {
    lines.push(
      `${codebaseAnalysis.ciWorkflows.length} CI workflow(s) detected`
    );
  }

  // Env vars
  if (codebaseAnalysis.envVars.length > 0) {
    lines.push(
      `${codebaseAnalysis.envVars.length} environment variables from .env.example`
    );
  }

  // Project docs
  if (codebaseAnalysis.projectDocs.length > 0) {
    const docNames = codebaseAnalysis.projectDocs.map(
      (d: ExistingRule) => d.source
    );
    lines.push(`Project docs found: ${docNames.join(", ")}`);
  }

  // Conventions count (rough proxy: existing rules + stack items)
  const conventionCount =
    codebaseAnalysis.existingRules.length +
    codebaseAnalysis.buildCommands.length +
    codebaseAnalysis.lintCommands.length;
  if (conventionCount > 0) {
    lines.push(`${conventionCount} project conventions extracted`);
  }

  // Retry / failure patterns from sessions
  if (sessionAnalysis && sessionAnalysis.retryCount > 0) {
    lines.push(
      `${sessionAnalysis.retryCount} prompt retries detected (failure patterns)`
    );
  }

  return lines.map((l) => `• ${l}`).join("\n");
}

